import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { inspect } from 'node:util'
import { transform } from 'lightningcss'

const root = process.cwd()
const sourcePath = resolve(root, 'src/lib/jukette-player.css')
const outputPath = resolve(root, 'src/lib/jukette-player.css.generated.ts')

const source = await readFile(sourcePath)
const { code } = transform({
	code: source,
	filename: sourcePath,
	minify: true,
})
const style = code.toString()
const chunks = []
for (let index = 0; index < style.length; index += 80) {
	chunks.push(style.slice(index, index + 80))
}

const output = `// Generated by bin/build-player-css.mjs from src/lib/jukette-player.css.
export const playerStyles = [
${chunks.map((chunk) => `\t${inspect(chunk)},`).join('\n')}
].join('')
`

await mkdir(dirname(outputPath), { recursive: true })
await writeFile(outputPath, output)
